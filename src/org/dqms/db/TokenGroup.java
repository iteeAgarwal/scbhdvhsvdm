package org.dqms.db;

public class TokenGroup {

	public int token_group_id;
	public int depart_id;
	public String room_id;
	public String token_group_name;
	
	
	public int getToken_group_id() {
		return token_group_id;
	}
	public void setToken_group_id(int token_group_id) {
		this.token_group_id = token_group_id;
	}
	public int getDepart_id() {
		return depart_id;
	}
	public void setDepart_id(int depart_id) {
		this.depart_id = depart_id;
	}
	public String getRoom_id() {
		return room_id;
	}
	public void setRoom_id(String room_id) {
		this.room_id = room_id;
	}
	public String getToken_group_name() {
		return token_group_name;
	}
	public void setToken_group_name(String token_group_name) {
		this.token_group_name = token_group_name;
	}
	
	

}
