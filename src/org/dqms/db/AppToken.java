package org.dqms.db;

public class AppToken {
	
	
	private int app_token_id;
	private String app_token_value;
	private int tokenNo;
	private int appWalk;
	
	public int getTokenNo() {
		return tokenNo;
	}
	public void setTokenNo(int tokenNo) {
		this.tokenNo = tokenNo;
	}
	public int getAppWalk() {
		return appWalk;
	}
	public void setAppWalk(int appWalk) {
		this.appWalk = appWalk;
	}
	public int getApp_token_id() {
		return app_token_id;
	}
	public void setApp_token_id(int app_token_id) {
		this.app_token_id = app_token_id;
	}
	public String getApp_token_value() {
		return app_token_value;
	}
	public void setApp_token_value(String app_token_value) {
		this.app_token_value = app_token_value;
	}
	
	
	

}
